#ifndef UTILS_CHARS_H
#define UTILS_CHARS_H

#include <stdbool.h>

/// @brief Checks whether a character is an alphabetic letter.
/// @param c
/// @return true if the character is an alphabetic letter, false otherwise.
bool is_alpha(char c);

/// @brief Checks whether a character is a digit.
/// @param c
/// @return true if the character is a digit, false otherwise.
bool is_digit(char c);

#endif // UTILS_CHARS_H
